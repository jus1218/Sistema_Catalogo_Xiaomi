    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        // TODO add your handling code here:
        if (verificarVacio()) {
            JOptionPane.showMessageDialog(this, service.addC(getData()));
            loadData();

        } else {
            JOptionPane.showMessageDialog(this, "Llene todos campos");
        }
        clearInputs();
    }//GEN-LAST:event_btnCrearActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String[] data = service.find(getData()[0]);
        if (data != null && data[0] != null) {
            setData(data);
        } else {
            JOptionPane.showMessageDialog(this, "Usuario no encontrado en la base de datos");
            this.clearInputs();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (verificarVacio()) {
            JOptionPane.showMessageDialog(this, service.deleteC(getData()[0]));
            loadData();

        } else {
            JOptionPane.showMessageDialog(this, "Usuario no encontrado en la base de datos");
        }
        clearInputs();
    }//GEN-LAST:event_btnEliminarActionPerformed

