    public void loadData() {
        try {
            String[] headers = new String[]{"Id", "Nombre",
                "Apellido", "Email", "Telefono", "Numero registro"};
            DefaultTableModel tb = new DefaultTableModel(service.getAll(), headers);
            this.tblComprador.setModel(tb);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error al leer la base de datos [" + ex.getMessage() + "]");
        }
    }

    public static void entraMouseInput(JTextField txt, String text) {
        if (txt.getText().equals(text)) {
            txt.setText("");
        }

    }

    public static void saleMouseInput(JTextField txt, String text) {
        if (txt.getText().isEmpty()) {
            txt.setText(text);
        }

    }

    public String[] getData() {//contiene el arreglo que se envia para el controller
        String[] data = {
            this.txtId.getText(),
            this.txtNombre.getText(),
            this.txtApellido.getText(),
            this.txtEmail.getText(),
            this.txtTelefono.getText(),};
        return data;
    }

    public void setData(String[] data) {//llena los campos con los datos que le envia la DB
        this.txtId.setText(data[0]);
        this.txtNombre.setText(data[1]);
        this.txtApellido.setText(data[2]);
        this.txtEmail.setText(data[3]);
        this.txtTelefono.setText(data[4]);
    }

